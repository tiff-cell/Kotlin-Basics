fun main() {
    //Arithmetic Operators
    var num1=30
    var num2=5

    println(num1 + num2)
    println(num1 - num2)
    println(num1 * num2)
    println(num1 / num2)
    println(num1 % num2)

    //Comparison Operators

    println(num1 < num2)
    println(num1 > num2)
    println(num1 <= num2)
    println(num1 >= num2)
    println(num1 == num2)
    println(num1 != num2)

    //Assignment Operators-Used to assign values to variables

    var x = 90
    x += 4
    println(x)

    //Logical Operators
    var log = 56
    println(log<100 && log > 89)
    println(log<100 || log > 89)
    println(!(log<100 || log > 89))




}