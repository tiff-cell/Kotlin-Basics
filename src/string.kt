fun main() {
    var text ="Hello World"
    var firstname ="John"
    var lastname= "Osiya"


    println(text)
    //Using Index
    println(text[1])

    //String Concatenation
    println(firstname + " " + lastname)

    //Modifying a string
    println(text.uppercase())

    println(firstname.lowercase())

    //String Interpolation
    println("My Fullname is $firstname $lastname")



}